#include "fdf.h"

void	ft_centermap(t_env *m)
{
	int i;
	int j;

	i = 0;
	while (i < m->rows)
	{
		j = 0;
		while (j < m->columns)
		{
			(m->map2)[i][j].x -= (m->columns / 2);
			(m->map2)[i][j].y -= (m->rows / 2);
			j++;
		}
		i++;
	}
}
